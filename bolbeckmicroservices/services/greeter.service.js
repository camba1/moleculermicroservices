		/**
		 * Say a 'Hello'
		 *
		 * @returns
		 */
		async hello(ctx) {
			const payload = `Hello from the greeter at ${this.broker.nodeID}`
			const num = await ctx.call("helper.random")
			ctx.emit("hello_called", { payload, num })
			return { payload, num };
		},

		/**
		 * Welcome a username
		 *
		 * @param {String} name - User name
		 */
		welcome: {
			params: {
				name: "string"
			},
			handler(ctx) {
				return `Welcome, ${ctx.params.name}`;
			}
		}
